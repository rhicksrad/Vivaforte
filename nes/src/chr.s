; ============================================================
; chr.s — pattern tables (all art lives here as pixel strings)
;   chars '0'-'3' = palette index per pixel
; ============================================================

; --- one row of one plane from a pixel string ---
.macro tilerow s, plane, off
_v .set 0
.repeat 8, i
_v .set _v | ((((.strat(s, off+i) - $30) >> plane) & 1) << (7 - i))
.endrep
.byte _v
.endmacro

; --- one 8x8 tile (both planes) from 8 strings at column offset ---
.macro t8 r0,r1,r2,r3,r4,r5,r6,r7,off
tilerow r0,0,off
tilerow r1,0,off
tilerow r2,0,off
tilerow r3,0,off
tilerow r4,0,off
tilerow r5,0,off
tilerow r6,0,off
tilerow r7,0,off
tilerow r0,1,off
tilerow r1,1,off
tilerow r2,1,off
tilerow r3,1,off
tilerow r4,1,off
tilerow r5,1,off
tilerow r6,1,off
tilerow r7,1,off
.endmacro

; --- background tile from 8 strings of 8 ---
.macro bgtile r0,r1,r2,r3,r4,r5,r6,r7
t8 r0,r1,r2,r3,r4,r5,r6,r7,0
.endmacro

; --- 8x16 sprite pair from 16 strings of 8 ---
.macro sprite8x16 r0,r1,r2,r3,r4,r5,r6,r7,r8,r9,r10,r11,r12,r13,r14,r15
t8 r0,r1,r2,r3,r4,r5,r6,r7,0
t8 r8,r9,r10,r11,r12,r13,r14,r15,0
.endmacro

; --- 16x16 object (2 pairs: left col, right col) from 16 strings of 16 ---
.macro sprite16x16 r0,r1,r2,r3,r4,r5,r6,r7,r8,r9,r10,r11,r12,r13,r14,r15
t8 r0,r1,r2,r3,r4,r5,r6,r7,0
t8 r8,r9,r10,r11,r12,r13,r14,r15,0
t8 r0,r1,r2,r3,r4,r5,r6,r7,8
t8 r8,r9,r10,r11,r12,r13,r14,r15,8
.endmacro

; --- BG font glyph: 7 numeric rows (5-bit), color 3 (both planes) ---
.macro glyph b0,b1,b2,b3,b4,b5,b6
.byte (b0<<3),(b1<<3),(b2<<3),(b3<<3),(b4<<3),(b5<<3),(b6<<3),0
.byte (b0<<3),(b1<<3),(b2<<3),(b3<<3),(b4<<3),(b5<<3),(b6<<3),0
.endmacro

; --- sprite letter pair: glyph in top tile, color 1 ---
.macro sglyph b0,b1,b2,b3,b4,b5,b6
.byte (b0<<3),(b1<<3),(b2<<3),(b3<<3),(b4<<3),(b5<<3),(b6<<3),0
.byte 0,0,0,0,0,0,0,0
.res 16
.endmacro

